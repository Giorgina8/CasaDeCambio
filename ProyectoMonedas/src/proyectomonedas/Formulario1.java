    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //  Boton Aceptar.
        String x;
        double cantidad, precio, valorTotal;
        
        DecimalFormat df1 = new DecimalFormat( ",###.00" );

        cantidad = Double.parseDouble(jTextField1.getText());
        if( jLabel9.getText() != "" )
           precio = Double.parseDouble(jLabel9.getText());
        else
           precio = Double.parseDouble(jLabel10.getText());

        valorTotal= cantidad*precio;
        if( jRadioCompra.isSelected() )
           jLabel3.setText( "$ " + df1.format( valorTotal  ) );
        //   jLabel3.setText( "$ " + df1.format( String.valueOf( valorTotal ) ) );
        
        else
            if( jRadioVenta.isSelected() )
                   jLabel3.setText( jLabel8.getText() + " " +String.valueOf( valorTotal ) );
      
    }//GEN-LAST:event_jButton3ActionPerformed

